let numero3 = 7.0;  // Declaração da variável numero3
let numero4 = 8.0; // Declaração da variável numero4  

console.log(`Os números são:
     ${numero1} 
     ${numero2} 
     ${numero3} 
     ${numero4} `); // ler os números

