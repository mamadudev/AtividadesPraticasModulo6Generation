
let nota1 = 10.0; // Declaração da variável nota1
let nota2 = 8.0; // Declaração da variável nota2
let nota3 = 7.0; // Declaração da variável nota3
let nota4 = 7.5; // Declaração da variável nota4

console.log(`As notas são: 
     ${nota1.toFixed(1)} 
     ${nota2.toFixed(1)} 
     ${nota3.toFixed(1)} 
     ${nota4} `); // ler as notas

let media; // Declaração da variável media  
media = (nota1 + nota2 + nota3 + nota4) / 4;
console.log(`A média das notas é: ${media.toFixed(1)}`); // calcular e ler a média

